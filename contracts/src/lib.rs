#![no_std]

//! ZK Shield — Soroban Smart Contract
//!
//! Private payment pool on Stellar.
//! Verifies Groth16 (BN254) ZK proofs using Stellar Protocol X-Ray host functions.
//!
//! Entry points:
//!   initialize(admin, xlm_token, verifying_key)
//!   deposit(depositor, commitment, amount)  → leaf_index: u32
//!   spend(caller, proof, nullifier, new_commitment, recipient, amount)
//!   get_root()        → BytesN<32>
//!   get_leaf_count()  → u32
//!   is_spent(nullifier) → bool

use soroban_sdk::{
    contract, contractimpl, contracttype,
    Address, Bytes, BytesN, Env, Vec,
    symbol_short, token,
};

// ─── Storage keys ─────────────────────────────────────────────────────────────

#[contracttype]
#[derive(Clone)]
pub enum DataKey {
    Root,
    LeafCount,
    FilledSubtrees(u32),
    Nullifier(BytesN<32>),
    VerifyingKey,
    Admin,
    XlmToken,
}

